public class Execution {
    private String str;
    private String re;

    public Execution() {
    }

    public Execution(String str, String re) {
        this.str = str;
        this.re = re;
    }

    public String getStr() {
        return str;
    }

    public void setStr(String str) {
        this.str = str;
    }

    public String getRe() {
        return re;
    }

    public void setRe(String re) {
        this.re = re;
    }
    
}
